public class ThingListBean {
    private String id;
    private String title;
    private String info;
    private String userid;//收藏时需要用到
    private boolean isCollect;//是否收藏
    private String date;
    private String starSize;//星星
    private String dafu;//咨询投诉用
    private String serverObject;//服务对象（个人或者法人）

    public String getServerObject() {
        return serverObject;
    }

    public void setServerObject(String serverObject) {
        this.serverObject = serverObject;
    }

    public String getStarSize() {
        return starSize;
    }

    public void setStarSize(String starSize) {
        this.starSize = starSize;
    }

    public String getDafu() {
        return dafu;
    }

    public void setDafu(String dafu) {
        this.dafu = dafu;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public boolean isCollect() {
        return isCollect;
    }

    public void setCollect(boolean collect) {
        isCollect = collect;
    }

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public boolean getIsCollect() {
        return isCollect;
    }

    public void setIsCollect(boolean isCollect) {
        this.isCollect = isCollect;
    }
}
